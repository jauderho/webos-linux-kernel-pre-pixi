int
omap34xx_isp_dbg_register_device(struct device *dev)
{
	int i;
	int rc;
	struct device_attribute *attr;

	for (i = 0, rc = 0; ARRAY_SIZE(omap34xx_isp_dbg_attrs) > i; ++i) {
		attr = &omap34xx_isp_dbg_attrs[i].attr;
		if ((rc = device_create_file(dev, attr))) {
			while (i--) {
				attr = &omap34xx_isp_dbg_attrs[i].attr;
				device_remove_file(dev, attr);
			}
			break;
		}
	}

	return (rc);
}

EXPORT_SYMBOL(omap34xx_isp_dbg_register_device);

void
omap34xx_isp_dbg_unregister_device(struct device *dev)
{
	int i;
	struct device_attribute *attr;

	for (i = 0; ARRAY_SIZE(omap34xx_isp_dbg_attrs) > i; ++i) {
		attr = &omap34xx_isp_dbg_attrs[i].attr;
		device_remove_file(dev, attr);
	}
}

EXPORT_SYMBOL(omap34xx_isp_dbg_unregister_device);
